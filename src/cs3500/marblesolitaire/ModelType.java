package cs3500.marblesolitaire;

/**
 * The interface ModelTypes is used to represent the different shapes of
 * marble solitaire boards as enums.
 */
public interface ModelType {
  /**
   * Enum containing the current available shapes of marble solitaire boards
   * used in the main method.
   */
  enum ModelShape { ENGLISH, EUROPEAN, TRIANGULAR }

}
